package com.board.model;

public class BoardDTO {
	
	private int board_index; 
	private String board_title;
	private String board_cont;
	private String board_writer;
	private String board_type;
	private String board_heading;
	private int board_hit;
	private int board_thumbs;
	private String board_date;
	
	
	
	public int getBoard_index() {
		return board_index;
	}
	public void setBoard_index(int board_index) {
		this.board_index = board_index;
	}
	public String getBoard_title() {
		return board_title;
	}
	public void setBoard_title(String board_title) {
		this.board_title = board_title;
	}
	public String getBoard_cont() {
		return board_cont;
	}
	public void setBoard_cont(String board_cont) {
		this.board_cont = board_cont;
	}
	public String getBoard_writer() {
		return board_writer;
	}
	public void setBoard_writer(String board_writer) {
		this.board_writer = board_writer;
	}
	public String getBoard_type() {
		return board_type;
	}
	public void setBoard_type(String board_type) {
		this.board_type = board_type;
	}
	public String getBoard_heading() {
		return board_heading;
	}
	public void setBoard_heading(String board_heading) {
		this.board_heading = board_heading;
	}
	public int getBoard_hit() {
		return board_hit;
	}
	public void setBoard_hit(int board_hit) {
		this.board_hit = board_hit;
	}
	public int getBoard_thumbs() {
		return board_thumbs;
	}
	public void setBoard_thumbs(int board_thumbs) {
		this.board_thumbs = board_thumbs;
	}
	public String getBoard_date() {
		return board_date;
	}
	public void setBoard_date(String board_date) {
		this.board_date = board_date;
	}
	
	
	
}
